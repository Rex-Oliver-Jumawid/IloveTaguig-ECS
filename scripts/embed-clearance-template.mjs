import { readFile, writeFile } from 'node:fs/promises'

const input = 'assets/templates/acroformtemplates/BARANGAY_CLEARANCE_runtime_template.pdf'
const output = 'supabase/functions/generate-clearance/template.ts'
const base64 = (await readFile(input)).toString('base64')

await writeFile(output, `// Generated by scripts/embed-clearance-template.mjs.\nexport const templateBytes = Uint8Array.from(atob('${base64}'), (character) => character.charCodeAt(0))\n`)
console.log(`Embedded ${input} in ${output}.`)
